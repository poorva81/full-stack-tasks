// 1. if Statement
let age = 20;
if (age >= 18) {
    console.log("You are eligible to vote.");
}

// 2. if...else Statement
let isRaining = true;
if (isRaining) {
    console.log("Take an umbrella.");
} else {
    console.log("No need for an umbrella.");
}

// 3. if...else if...else Statement
let score = 85;
if (score >= 90) {
    console.log("Grade: A");
} else if (score >= 80) {
    console.log("Grade: B");
} else if (score >= 70) {
    console.log("Grade: C");
} else {
    console.log("Grade: D or below");
}


